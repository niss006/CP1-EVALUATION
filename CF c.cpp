#include <bits/stdc++.h>
using namespace std;

int main() {
    long long X, Y;
    if (!(cin >> X >> Y)) return 0;

    cout << X << " + " << Y << " = " << (X + Y) << '\n';
    cout << X << " * " << Y << " = " << (X * Y) << '\n';
    cout << X << " - " << Y << " = " << (X - Y) << '\n';

    return 0;
}
